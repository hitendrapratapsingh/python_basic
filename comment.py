print('hello world \'my name\'')  # use single quotes under single quote
print("hello world \"my name\"")  # use double quotes under double quote
print("Hi i am hitendra \n okk\n mdmdm") # use \n for line break
print("Hi i am gaurav \t hitendra")  # use \t for space like tab
print("Hi i am gaurav \\") # use \\ for print single \ (backslash)
print("Hi i am gaurav \\\\") #use \\\\ for double backslash print
print("Hi i am helllo\b") # useless